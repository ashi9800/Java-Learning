package Day_04_switch;

import java.util.Scanner;

public class Day05_switch_using_scanner {

	public static void main(String[] args) {
		
	Scanner getinput=new Scanner(System.in);
	System.out.println("give me the value");
	
	int number = getinput.nextInt();

	
	switch(number) {
	case 1:
		System.out.println("print number 1 ");
		break;
	case 2:
		System.out.println("print number 2");
		break;
	case 3:
		System.out.println("print number 3 ");
		break;
	case 4:
		System.out.println("print number 4 ");
		break;
	case 5:
		System.out.println("print number 5 ");
		break;
		
		
		
		
		
		
		
	}

	}

}
